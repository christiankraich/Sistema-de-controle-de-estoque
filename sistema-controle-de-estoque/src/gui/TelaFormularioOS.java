    private void cbNomeClienteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbNomeClienteItemStateChanged
        String nome = cbNomeCliente.getSelectedItem().toString();
        
        if (nome.equals("Selecione o cliente")) {
            txtTelefone.setText("");
            txtCPF.setText("");
            txtEndereco.setText("");
            return;
        }
        
        ClientesDAO clientesDao = new ClientesDAO(conn);
        Clientes cliente = clientesDao.buscarNomeCliente(nome);
        if (cliente != null) {
            txtTelefone.setText(cliente.getTelefone());
            txtCPF.setText(cliente.getCpf());
            txtEndereco.setText(cliente.getEnderecoCompleto());
        }
        
    }//GEN-LAST:event_cbNomeClienteItemStateChanged

